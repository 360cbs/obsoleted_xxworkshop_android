public final class Rect {
    public int x;
    public int y;
    public int w;
    public int h;

    public Rect() {

    }

    public Rect(int x, int y, int w, int h) {
        this.x = x;
        this.y = y;
        this.w = w;
        this.h = h;
    }
}
